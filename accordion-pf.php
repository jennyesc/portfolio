    <section class="pf-dev-cont">
        <h4>Development</h4>
        <p>Once the designs were complete, I started by creating the content, such as the images used throughout the project. 
        After the content creation, I started entering all of the HTML code, using HTML5 semantics. Once all of the content was placed, I 
        started styling the website, using CSS/Sass, JavaScript and JQuery.
        </p>
        
        <h4>Launch</h4>
        <p>When styling had been completed, I validated the code on my local server. After the code validation, I moved it on to my live server within a 
            sub-folder to test on various devices and debug the site.<br>
        Once the site's kinks had been worked out, I moved it to my root folder for launch.
    </section>

